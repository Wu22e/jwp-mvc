package core.mvc.tobe;

import core.annotation.web.RequestMethod;
import core.db.DataBase;
import next.model.User;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.DisplayName;
import org.junit.jupiter.api.Test;
import org.springframework.mock.web.MockHttpServletRequest;
import org.springframework.mock.web.MockHttpServletResponse;

import static org.assertj.core.api.Assertions.assertThat;

public class AnnotationHandlerMappingTest {
    private AnnotationHandlerMapping handlerMapping;

    @BeforeEach
    public void setup() {
        handlerMapping = new AnnotationHandlerMapping("core.mvc.tobe");
        handlerMapping.initialize();
    }

    @Test
    @DisplayName("GET 메서드, /users 경로의 특정 유저를 찾는다.")
    public void createFind() throws Exception {
        User user = new User("pobi", "password", "포비", "pobi@nextstep.camp");
        createUser(user);
        assertThat(DataBase.findUserById(user.getUserId())).isEqualTo(user);

        MockHttpServletRequest request = new MockHttpServletRequest("GET", "/users");
        request.setParameter("userId", user.getUserId());
        MockHttpServletResponse response = new MockHttpServletResponse();
        HandlerExecution execution = handlerMapping.getHandler(request);
        execution.handle(request, response);

        assertThat(request.getAttribute("user")).isEqualTo(user);
    }

    private void createUser(User user) throws Exception {
        MockHttpServletRequest request = new MockHttpServletRequest("POST", "/users");
        request.setParameter("userId", user.getUserId());
        request.setParameter("password", user.getPassword());
        request.setParameter("name", user.getName());
        request.setParameter("email", user.getEmail());
        MockHttpServletResponse response = new MockHttpServletResponse();
        HandlerExecution execution = handlerMapping.getHandler(request);
        execution.handle(request, response);
    }

    @Test
    @DisplayName("메서드 지정 되어있지 않을 경우, 모든 메서드를 지원한다.")
    void allMethod() throws Exception {
        for (RequestMethod requestMethod : RequestMethod.values()) {
            MockHttpServletRequest request = new MockHttpServletRequest(requestMethod.name(), "/all/method");
            MockHttpServletResponse response = new MockHttpServletResponse();
            HandlerExecution execution = handlerMapping.getHandler(request);
            execution.handle(request, response);
            assertThat(request.getAttribute("method")).isEqualTo("all");
        }
    }
}
